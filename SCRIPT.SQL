CREATE DATABASE SISACADEMICO;

USE SISACADEMICO;

CREATE TABLE TBLDISCIPLINAS(

	ID INT IDENTITY(1,1) PRIMARY KEY,
	DISID INT,
	DISNOME VARCHAR(20),
	DISSIG VARCHAR(20),
	DISOBS VARCHAR(100)
);

CREATE TABLE TBLCURSOS(

	ID INT IDENTITY(1,1) PRIMARY KEY,
	CURSOID INT,
	CURSONOME VARCHAR(20),
	CURSOSIG VARCHAR(20),
	CURSOOBS VARCHAR(100)
);

CREATE TABLE TBLPERIODOS(

	ID INT IDENTITY(1,1) PRIMARY KEY,
	PERID INT,
	PERNOME VARCHAR(20),
	PERSIG VARCHAR(20)
);
